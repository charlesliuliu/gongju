def update_file(filepath, additions_tools, additions_guides, calc_data, bf_page, lc_page, ls_page):
    with open(filepath, 'r', encoding='utf-8') as f:
        data = json.load(f)
    # Update tools
    if 'tools' not in data:
        data['tools'] = {}
    data['tools'].update(additions_tools)
    # Update guides
    if 'guides' not in data:
        data['guides'] = {}
    data['guides'].update(additions_guides)
    # Add new namespaces
    data['lumberCalculator'] = calc_data
    data['boardFeetPage'] = bf_page
    data['lumberCalcPage'] = lc_page
    data['lumberSizesPage'] = ls_page
    with open(filepath, 'w', encoding='utf-8') as f:
        json.dump(data, f, indent=2, ensure_ascii=False)
    print(f"Updated: {filepath}")


